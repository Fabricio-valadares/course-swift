import UIKit

// variaveis com "var" tem a possibilidade de mudar de valor com o mesmo tipo iniciado.
// variaveis com "let" sao as constante, portanto ela nao pode mudar o valor.

var name = "Fabricio"

// para dar print no terminal com swift utiliza o "print"

print(name)

// --------------------------------------------

// para usar um templete string no swift usamos o "\()" dentro de aspas duplas.

let fruit = "banana"

var textChange = "name de fruta \(fruit)"

print(textChange)


